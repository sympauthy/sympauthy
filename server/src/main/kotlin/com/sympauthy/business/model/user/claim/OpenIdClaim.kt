package com.sympauthy.business.model.user.claim

import com.sympauthy.business.model.user.OpenIdConnectScope
import com.sympauthy.business.model.user.claim.ClaimDataType.DATE
import com.sympauthy.business.model.user.claim.ClaimDataType.STRING
import com.sympauthy.business.model.user.claim.ClaimDataType.TIMEZONE
import com.sympauthy.business.model.user.claim.ClaimGroup.ADDRESS
import com.sympauthy.business.model.user.claim.ClaimGroup.IDENTITY

/**
 * Enumeration of claims, defined in the OpenID specification, that are supported by this application.
 *
 * @see <a href="https://openid.net/specs/openid-connect-core-1_0.html#StandardClaims">Standard claims</a>
 */
enum class OpenIdClaim(
    val id: String,
    val verifiedId: String? = null,
    val type: ClaimDataType,
    /**
     * Group the claim is part of.
     * Claims sharing the same [group] are related one to another.
     * Ex. first name & last name.
     */
    val group: ClaimGroup? = null,
    /**
     * Scope that must be requested by the client to disclose the claim through the user endpoint.
     */
    val scope: OpenIdConnectScope,
    /**
     * Is the value of this claim is generated by authentication server?
     */
    val generated: Boolean = false
) {
    SUBJECT(
        id = Id.SUB,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE,
        generated = true
    ),
    NAME(
        id = Id.NAME,
        type = STRING,
        group = IDENTITY,
        scope = OpenIdConnectScope.PROFILE
    ),
    GIVEN_NAME(
        id = Id.GIVEN_NAME,
        type = STRING,
        group = IDENTITY,
        scope = OpenIdConnectScope.PROFILE
    ),
    FAMILY_NAME(
        id = Id.FAMILY_NAME,
        type = STRING,
        group = IDENTITY,
        scope = OpenIdConnectScope.PROFILE
    ),
    MIDDLE_NAME(
        id = Id.MIDDLE_NAME,
        type = STRING,
        group = IDENTITY,
        scope = OpenIdConnectScope.PROFILE
    ),
    NICKNAME(
        id = Id.NICKNAME,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    PREFERRED_USERNAME(
        id = Id.PREFERRED_USERNAME,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    PROFILE(
        id = Id.PROFILE,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    PICTURE(
        id = Id.PICTURE,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    WEBSITE(
        id = Id.WEBSITE,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    EMAIL(
        id = Id.EMAIL,
        verifiedId = Id.EMAIL_VERIFIED,
        type = ClaimDataType.EMAIL,
        scope = OpenIdConnectScope.EMAIL
    ),
    GENDER(
        id = Id.GENDER,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    BIRTH_DATE(
        id = Id.BIRTH_DATE,
        type = DATE,
        scope = OpenIdConnectScope.PROFILE
    ),
    ZONE_INFO(
        id = Id.ZONE_INFO,
        type = TIMEZONE,
        scope = OpenIdConnectScope.PROFILE
    ),
    LOCALE(
        id = Id.LOCALE,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE
    ),
    PHONE_NUMBER(
        id = Id.PHONE_NUMBER,
        verifiedId = Id.PHONE_NUMBER_VERIFIED,
        type = ClaimDataType.PHONE_NUMBER,
        scope = OpenIdConnectScope.PHONE
    ),
    UPDATED_AT(
        id = Id.UPDATED_AT,
        type = STRING,
        scope = OpenIdConnectScope.PROFILE,
        generated = true
    ),
    STREET_ADDRESS(
        id = Id.STREET_ADDRESS,
        type = STRING,
        group = ADDRESS,
        scope = OpenIdConnectScope.ADDRESS
    ),
    LOCALITY(
        id = Id.LOCALITY,
        type = STRING,
        group = ADDRESS,
        scope = OpenIdConnectScope.ADDRESS
    ),
    REGION(
        id = Id.REGION,
        type = STRING,
        group = ADDRESS,
        scope = OpenIdConnectScope.ADDRESS
    ),
    POSTAL_CODE(
        id = Id.POSTAL_CODE,
        type = STRING,
        group = ADDRESS,
        scope = OpenIdConnectScope.ADDRESS
    ),
    COUNTRY(
        id = Id.COUNTRY,
        type = STRING,
        group = ADDRESS,
        scope = OpenIdConnectScope.ADDRESS
    );

    object Id {
        const val SUB = "sub"
        const val NAME = "name"
        const val GIVEN_NAME = "given_name"
        const val FAMILY_NAME = "family_name"
        const val MIDDLE_NAME = "middle_name"
        const val NICKNAME = "nickname"
        const val PREFERRED_USERNAME = "preferred_username"
        const val PROFILE = "profile"
        const val PICTURE = "picture"
        const val WEBSITE = "website"
        const val EMAIL = "email"
        const val EMAIL_VERIFIED = "email_verified"
        const val GENDER = "gender"
        const val BIRTH_DATE = "birth_date"
        const val ZONE_INFO = "zoneinfo"
        const val LOCALE = "locale"
        const val PHONE_NUMBER = "phone_number"
        const val PHONE_NUMBER_VERIFIED = "phone_number_verified"
        const val UPDATED_AT = "updated_at"
        const val STREET_ADDRESS = "street_address"
        const val LOCALITY = "locality"
        const val REGION = "region"
        const val POSTAL_CODE = "postal_code"
        const val COUNTRY = "country"
    }
}
